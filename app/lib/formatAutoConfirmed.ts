import type { MatchId } from '../data/teams'
import type { MatchDiscipline } from './fairPlay'
import type { ConfirmedGroupPick } from './computeBracketQualifiers'

export interface AutoConfirmedData {
  groups: Partial<Record<string, ConfirmedGroupPick>>
  scores: Record<number, { home: number; away: number }>
  discipline: Record<number, MatchDiscipline>
  matches: Partial<Record<MatchId, string>>
  syncedAt: string
  source: string
}

export function formatAutoConfirmedTs(data: AutoConfirmedData): string {
  return `// Auto-generated by scripts/sync-wc-results.ts — do not edit by hand
import type { GroupKey, MatchId } from './teams'
import type { MatchDiscipline } from '../lib/fairPlay'

export const AUTO_CONFIRMED = {
  groups: ${JSON.stringify(data.groups, null, 2)} as Partial<Record<GroupKey, {
    first?: string
    second?: string
    third?: string
    thirdSlot?: MatchId
  }>>,
  scores: ${JSON.stringify(data.scores, null, 2)} as Record<number, { home: number; away: number }>,
  discipline: ${JSON.stringify(data.discipline, null, 2)} as Record<number, MatchDiscipline>,
  matches: ${JSON.stringify(data.matches, null, 2)} as Partial<Record<MatchId, string>>,
  syncedAt: ${JSON.stringify(data.syncedAt)},
  source: ${JSON.stringify(data.source)},
}
`
}
